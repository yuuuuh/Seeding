function tap_click(hidden1,hidden2,show){
	var1 = document.getElementById(hidden1);
	var2 = document.getElementById(hidden2);
	var3 = document.getElementById(show);

	var1.style.visibility = "hidden";
	var2.style.visibility = "hidden";
	var3.style.visibility = "visible";
	this.style.fontWeight = "bold";
	this.style.color = "#1cb97c";
	this.style.borderBottom = "4px";
	this.style.borderBottomColor = "#1cb97c";}
